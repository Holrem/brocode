# Typecasting = the process of converting a variable from one data type to another
#               str(), int(), float(), bool()

name = "Holrem"
age = 37
gpa = 3.0
is_student = True

print(type(name))
print(type(age))
print(type(is_student))

name = bool(name)
print(name)

age = float(age)
print(age)

gpa = int(gpa)
print(gpa)

is_student = str(is_student)
print(is_student)



